#1Crie um exemplo de cada uma das principais tipagens de variáveis.

# Definindo duas variáveis inteiras
numero1 = 10
numero2 = 5

# Realizando algumas operações matemáticas
soma = numero1 + numero2
subtracao = numero1 - numero2
multiplicacao = numero1 * numero2
divisao = numero1 / numero2

# Imprimindo os resultados
print("Soma:", soma)
print("Subtração:", subtracao)
print("Multiplicação:", multiplicacao)
print("Divisão:", divisao)

#2Crie uma solicitação de dados ao usuário.

# Solicitando dados ao usuário
nome = input("Digite seu nome: ")
idade = int(input("Digite sua idade: "))
altura = float(input("Digite sua altura (em metros): "))

# Exibindo as informações fornecidas pelo usuário
print("Nome:", nome)
print("Idade:", idade, "anos")
print("Altura:", altura, "metros")

